        [HttpPut("deletes")]
        public async Task<IActionResult> DeleteTags([FromBody] int[] data)
        {
            DefaultResponse def = new DefaultResponse();
            //check role
            var identity = (ClaimsIdentity)User.Identity;
            int userId = int.Parse(identity.Claims.Where(c => c.Type == "UserId").Select(c => c.Value).SingleOrDefault());
            int companyId = int.Parse(identity.Claims.Where(c => c.Type == "CompanyId").Select(c => c.Value).SingleOrDefault());
            string access_key = identity.Claims.Where(c => c.Type == "AccessKey").Select(c => c.Value).SingleOrDefault();
            if (!CheckRole.CheckRoleByCode(access_key, functionCode, (int)Const.Action.DELETED) && !CheckRole.CheckRoleByCode(access_key, functionCode1, (int)Const.Action.DELETED))
            {
                def.meta = new Meta(222, "No permission");
                return Ok(def);
            }

            try
            {
                if (!ModelState.IsValid)
                {
                    def.meta = new Meta(400, "Bad Request");
                    return Ok(def);
                }

                if (data == null)
                {
                    def.meta = new Meta(211, "Array Empty!");
                    return Ok(def);
                }

                if (data.Count() == 0)
                {
                    def.meta = new Meta(211, "Array Empty!");
                    return Ok(def);
                }

                using (var db = new IOITDataContext())
                {
                    for (int i = 0; i < data.Count(); i++)
                    {
                        Tag tag = await db.Tag.FindAsync(data[i]);

                        if (tag == null)
                        {
                            continue;
                        }

                        tag.Status = (int)Const.Status.DELETED;
                        db.Entry(tag).State = EntityState.Modified;
                        Models.EF.Action action = new Models.EF.Action();
                        action.ActionName = "Xoá tag “" + tag.Name + "”";
                        action.ActionType = (int)Const.ActionType.DELETE;
                        action.TargetId = tag.TagId.ToString();
                        action.TargetName = tag.Name;
                        action.CompanyId = companyId;
                        action.Logs = JsonConvert.SerializeObject(tag);
                        action.Time = 0;
                        action.Ipaddress = IpAddress();
                        action.Type = (int)Const.TypeAction.ACTION;
                        action.CreatedAt = DateTime.Now;
                        action.UserPushId = userId;
                        action.UserId = userId;
                        action.Status = (int)Const.Status.NORMAL;
                        await db.Action.AddAsync(action);
                        await db.SaveChangesAsync();
                        //Xóa TagMapping tương ứng
                        List<TagMapping> tagMappings = db.TagMapping.Where(tm => tm.TagId == tag.TagId && tm.Status != (int)Const.Status.DELETED).ToList();
                        tagMappings.ForEach(f => f.Status = (int)Const.Status.DELETED);
                    }

                    using (var transaction = db.Database.BeginTransaction())
                    {

                        try
                        {
                            await db.SaveChangesAsync();
                            transaction.Commit();
                            def.meta = new Meta(200, "Success");
                            def.data = data;
                            return Ok(def);
                        }
                        catch (DbUpdateException e)
                        {
                            transaction.Rollback();
                            log.Error("DbUpdateException:" + e);
                            def.meta = new Meta(500, "Internal Server Error");
                            return Ok(def);
                        }
                    }
                }
            }
            catch (Exception e)
            {
                log.Error("Error:" + e);
                def.meta = new Meta(500, "Internal Server Error");
                return Ok(def);
            }
        }

