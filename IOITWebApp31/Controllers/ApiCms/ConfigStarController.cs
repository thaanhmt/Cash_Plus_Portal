        // PUT: api/ConfigStar/5
        [HttpPut]
        public async Task<IActionResult> PutConfigStar([FromBody] List<ConfigStar> data)
        {
            DefaultResponse def = new DefaultResponse();
            //check role
            var identity = (ClaimsIdentity)User.Identity;
            string access_key = identity.Claims.Where(c => c.Type == "AccessKey").Select(c => c.Value).SingleOrDefault();
            int userId = int.Parse(identity.Claims.Where(c => c.Type == "UserId").Select(c => c.Value).SingleOrDefault());
            int companyId = int.Parse(identity.Claims.Where(c => c.Type == "CompanyId").Select(c => c.Value).SingleOrDefault());
            if (!CheckRole.CheckRoleByCode(access_key, functionCode, (int)Const.Action.UPDATE))
            {
                def.meta = new Meta(222, "No permission");
                return Ok(def);
            }
            try
            {
                if (!ModelState.IsValid)
                {
                    def.meta = new Meta(400, "Bad Request");
                    return Ok(def);
                }

                using (var db = new IOITDataContext())
                {
                    using (var transaction = db.Database.BeginTransaction())
                    {
                        try
                        {
                            foreach (var item in data)
                            {
                                var configS = db.ConfigStar.Where(e => e.ConfigStarId == item.ConfigStarId).FirstOrDefault();
                                if (configS != null)
                                {
                                    configS.StarColor = item.StarColor;
                                    configS.FromView = item.FromView;
                                    configS.ToView = item.ToView;
                                    configS.Operator = item.Operator;
                                    configS.FromDownload = item.FromDownload;
                                    configS.ToDownload = item.ToDownload;
                                    configS.UserId = userId;
                                    configS.UpdatedAt = DateTime.Now;
                                    db.ConfigStar.Update(configS);
                                }
                            }
                            await db.SaveChangesAsync();

                            if (data.Count > 0)
                            {
                                transaction.Commit();
                                Models.EF.Action action = new Models.EF.Action();
                                action.ActionName = "Sửa cấu hình sao ";
                                action.ActionType = (int)Const.ActionType.UPDATE;
                                action.TargetId = data.FirstOrDefault().WebsiteId.ToString();
                                action.TargetName = data.FirstOrDefault().WebsiteId.ToString();
                                action.CompanyId = companyId;
                                action.Logs = JsonConvert.SerializeObject(data);
                                action.Time = 0;
                                action.Ipaddress = IpAddress();
                                action.Type = (int)Const.TypeAction.ACTION;
                                action.CreatedAt = DateTime.Now;
                                action.UserPushId = userId;
                                action.UserId = userId;
                                action.Status = (int)Const.Status.NORMAL;
                                await db.Action.AddAsync(action);
                                await db.SaveChangesAsync();
                            }
                            else
                                transaction.Rollback();

                            def.meta = new Meta(200, "Success");
                            def.data = data;
                            return Ok(def);
                        }
                        catch (DbUpdateException e)
                        {
                            transaction.Rollback();
                            log.Error("DbUpdateException:" + e);
                            def.meta = new Meta(500, "Internal Server Error");
                            return Ok(def);
                        }
                    }
                }
            }
            catch (Exception e)
            {
                log.Error("Error:" + e);
                def.meta = new Meta(500, "Internal Server Error");
                return Ok(def);
            }
        }

